# Familiarizarse con las estructuras condicionales if, elif, else.
# Este código verifica la edad de una persona y muestra un mensaje correspondiente.
# Primero, se define la variable 'edad' con un valor de 18.

edad = 18

# Luego, se utiliza una estructura condicional para evaluar la edad.
# Si la edad es menor que 18, se imprime "Eres menor de edad."

if edad < 18:
 print("Eres menor de edad.")

# Si la edad es exactamente 18, se imprime "Tienes 18 años."

elif edad == 18:
 print("Tienes 18 años.")

# Si la edad es mayor que 18, se imprime "Eres mayor de edad."

else:
 print("Eres mayor de edad.")